    {
        $data = $request->validate([
            'isi' => 'required|max:120'
        ]);
        $data['user_id'] = $request->user()->id;
        $data['comment_id'] = $request->comment_id;
        Reply::create($data);

        notify()->success('Success add reply');
        return redirect()->back();
    }

